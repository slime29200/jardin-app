export function searchLocalPlants(query: string): PlantTemplate[] {
  if (!query.trim()) return PLANTS_DATABASE;
  const q = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
  return PLANTS_DATABASE.filter(p => {
    const name = p.name.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    const latin = (p.latinName || '').toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    const desc = (p.description || '').toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
    return name.includes(q) || latin.includes(q) || desc.includes(q);
  });
}
